module Type = struct
  let max_field_section_size = 0x06

  (* From RFC<HTTP3-RFC>§7.2.4.1:
   *   Setting identifiers of the format 0x1f * N + 0x21 for non-negative
   *   integer values of N are reserved to exercise the requirement that
   *   unknown identifiers be ignored. *)
  let unknown n = (0x1f * n) + 0x21

  let[@inline] unknown_n x = (x - 0x21) / 0x1f

  let is_unknown x = unknown_n x > 0
end

type t = { max_field_section_size : int }

let default = { max_field_section_size = Int.max_int }
